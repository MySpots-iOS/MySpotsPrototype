import UIKit

extension UIColor {
    class func mainLightGreen() -> UIColor {
        return UIColor( red:0.00, green:0.68, blue:0.69, alpha:1.0 )
    }
    
    class func mainDarkGreen() -> UIColor {
        return UIColor( red:0.00, green:0.57, blue:0.58, alpha:1.0 )
    }
    
    class func mainDarkBlack() -> UIColor {
        return UIColor( red:0.22, green:0.22, blue:0.22, alpha:1.0 )
    }
    
    class func mainLightGray() -> UIColor {
        return UIColor( red:0.22, green:0.22, blue:0.22, alpha:1.0 )
    }
}
